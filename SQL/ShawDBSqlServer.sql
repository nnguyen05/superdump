CREATE TABLE NN_EMPLOYEE 
	(EmployeeId    SMALLINT,
    Lname		VARCHAR (15) NOT NULL,
	Fname 	VARCHAR (15) NOT NULL, 
	PositionId 	SMALLINT NULL, 
	Supervisor	SMALLINT NULL,
    HireDate  	DATETIME       NULL,
	Salary	INT NULL,
    Commission	INT NULL,
    DeptId	SMALLINT NOT NULL,
	QualId	SMALLINT NULL,
