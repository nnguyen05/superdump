		PRIMARY KEY (EmployeeId),
	CONSTRAINT NN_EMPLOYEE_POSITIONID_FK FOREIGN KEY (PositionId)
		REFERENCES NN_POSITION (PositionId),
	CONSTRAINT NN_EMPLOYEE_DEPTID_FK FOREIGN KEY (DeptId)
		REFERENCES NN_DEPT (DeptId),
	CONSTRAINT NN_EMPLOYEE_QUALID_FK FOREIGN KEY (QualId)
		REFERENCES NN_QUALIFICATION (QualId));

